namespace SQLCompare.UI.Enums
{
    /// <summary>
    /// List of possible states of a button
    /// </summary>
    public enum ButtonState
    {
        /// <summary>
        /// Normal button
        /// </summary>
        Default,

        /// <summary>
        /// Makes the button appear pressed
        /// </summary>
        Active,

        /// <summary>
        /// Makes the button unclickable
        /// </summary>
        Disabled
    }
}
